#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	if (lst)
	{
		while (*lst)
		{
			temp = *lst;
			ft_lstdelone(*lst, del);
			*lst = temp->next;
		}
	}
}
